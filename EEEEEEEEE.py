# Usage: eeeEeee(Boolean) decides what ee(str) will return.
# If it is true, it will return Es. But if it's false, it will return e. 
# Enjoyeeeeeeeeeeeeeeeeeeeeeeeeeeee!

eE = True
Ee = False
eeE = str
eeeE = bool

class EEE:
    def __init__(self):
        self.e = eE

    def eeeEeee(self, ee: eeeE):
        self.e = ee

    def ee(self, eeee: str):
        EeEe = "E"
        if (self.e == Ee):
            EeEe = "e"
        eeeee = eeee.split(" ")
        eeeeee = []
        for e in eeeee:
            E = ""
            for ee in range(0, len(e)):
                E = E + EeEe
            eeeeee.append(E)
        return " ".join(eeeeee)